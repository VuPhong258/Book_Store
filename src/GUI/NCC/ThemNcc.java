    private void themNhaCungCap() throws IOException {
        if (!isValidData()) {
        return;
        }
        NhaCungCapDTO nccNew = getNewNCC();
        nhaCungCapBUS = new NhaCungCapBUS();
        boolean status = nhaCungCapBUS.themNhaCungCap(nccNew);
        if (status) {
            showMessage("Thêm nhà cung cấp thành công");
            dispose();  
        } else {
            showMessage("Thêm nhà cung cấp thất bại");
        }
    }
    
    

    
